// Boolean operator
// The Boolean primitive datatype represents a logical value, which can be either true or false.
// Boolean values are often used in conditional statements to control the flow of execution based on whether certain conditions are true or false.
let isSleeping = true;
let isRunning = false;
if (isSleeping) {
  console.log("He is sleeping");
} else {
  console.log("He is Running");
}
// output: He is sleeping

let x = 32;
let y = 40;
let isGreater = x > y;
console.log(isGreater); //false
